# Valid variable names
a = "mahi"
variable_name = "mahi"
variable_1 = "mahi"

print(a)
print(variable_name)
print(variable_1)


# More examples
name = "Mahi"
age = 21
price = 99.99
is_student = True

print(name, age, price, is_student)


# Case sensitivity
Name = "Uppercase"
name = "lowercase"

print(Name)
print(name)


# Multiple assignment
x, y, z = 1, 2, 3
print(x, y, z)


# Same value assignment
a = b = c = 10
print(a, b, c)

# Dynamic typing
var = 10
print(var)

var = "Now I am a string"
print(var)